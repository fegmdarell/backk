import mongoose from 'mongoose'

const EmpresaSchema = new mongoose.Schema({
  nombre: {
    type: String,
    required: true,
    trim: true
  },
  correo: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  password: {
    type: String,
    required: true
  }
}, {
  timestamps: true
})

const Empresa = mongoose.model('Empresa', EmpresaSchema)
export default Empresa
